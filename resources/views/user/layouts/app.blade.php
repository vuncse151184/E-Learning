                        function searchKeyUp() {
                            var lessons = @json($lessons);
                            var input = document.getElementById("myInput");
                            var filter = input.value.toUpperCase();
                            ul = document.getElementById("lessonList");

                            // If the filter is empty, hide the <ul> element and return
                            if (filter === "") {
                                ul.style.display = "none";
                                return;
                            }

                            var filteredLessons = lessons.filter(function(lesson) {
                                return lesson.lesson_name.toUpperCase().indexOf(filter) > -1;
                            });

                            var html = '';
                            for (i = 0; i < filteredLessons.length; i++) {
                                html += '<li><a href=" ' + window.location.origin + '/viewlesson/' + filteredLessons[i].id + '">' +
                                    filteredLessons[i].lesson_name + '</li>';
                            }
                            ul.innerHTML = html;
                            ul.style.display = "block";
                        }

                        // Add event listener to document for clicks
                        document.addEventListener('click', function(event) {
                            var searchZone = document.querySelector('.search-form-container');
                            if (!searchZone.contains(event.target)) {
                                hideList();
                            }
                        });
